function applyLang(code) {
  const t = i18n[code];
  if (!t) return;
  document.body.setAttribute("lang", code);
  document.documentElement.setAttribute("dir", t.dir);
  document.documentElement.setAttribute("lang", code);
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) el.textContent = t[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.getAttribute("data-i18n-html");
    if (t[key] !== undefined) el.innerHTML = t[key].replace(/\n\n/g, "<br><br>");
  });
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === code);
  });
  localStorage.setItem("kuklos-lang", code);
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => applyLang(btn.getAttribute("data-lang")));
  });
  const ham = document.querySelector(".hamburger");
  if (ham) ham.addEventListener("click", () => {
    document.querySelector(".nav-links").classList.toggle("open");
  });
  document.querySelectorAll(".nav-links a").forEach(a => {
    a.addEventListener("click", () => {
      document.querySelector(".nav-links").classList.remove("open");
    });
  });
  const saved = localStorage.getItem("kuklos-lang") || "ar";
  applyLang(saved);
});
